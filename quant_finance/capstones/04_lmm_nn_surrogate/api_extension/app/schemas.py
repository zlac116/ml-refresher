# =============================================================================
# /models, /models/{name}/promote
# =============================================================================
class ModelVersion(BaseModel):
    version:    int
    aliases:    list[str]
    created_at: datetime
    run_id:     str | None = None


class ModelsListResponse(BaseModel):
    name:     str
    versions: list[ModelVersion]


class PromoteRequest(BaseModel):
    version: int = Field(..., ge=1, examples=[1])
    alias:   str = Field(
        ...,
        min_length=1,
        pattern=r"^[a-z][a-z0-9_-]*$",
        examples=["production"],
    )


class PromoteResponse(BaseModel):
    name:    str
    version: int
    alias:   str
    note:    str = (
        "Alias updated in the registry. The running API still serves the "
        "PREVIOUSLY-loaded model — restart the service to pick up the new "
        "alias target."
    )
